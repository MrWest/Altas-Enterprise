using Res = System.Properties.Resources;

namespace System
{
    /// <summary>
    ///     An utility class to obtain localized versions of the week day names.
    /// </summary>
    public static class WeekDayNames
    {
        /// <summary>
        ///     Gets the localized name of Monday.
        /// </summary>
        public static string Monday
        {
            get { return Res.Monday; }
        }

        /// <summary>
        ///     Gets the localized name of Tuesday.
        /// </summary>
        public static string Tuesday
        {
            get { return Res.Tuesday; }
        }

        /// <summary>
        ///     Gets the localized name of Wednesday.
        /// </summary>
        public static string Wednesday
        {
            get { return Res.Wednesday; }
        }

        /// <summary>
        ///     Gets the localized name of Thurday.
        /// </summary>
        public static string Thurday
        {
            get { return Res.Thurday; }
        }

        /// <summary>
        ///     Gets the localized name of Friday.
        /// </summary>
        public static string Friday
        {
            get { return Res.Friday; }
        }

        /// <summary>
        ///     Gets the localized name of Saturday.
        /// </summary>
        public static string Saturday
        {
            get { return Res.Saturday; }
        }

        /// <summary>
        ///     Gets the localized name of Sunday.
        /// </summary>
        public static string Sunday
        {
            get { return Res.Sunday; }
        }
    }
}